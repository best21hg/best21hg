package koreait.day01;

public class C01_Print {

	//프로그램 실행하려면 main 매소드가필요하다
	public static void main(String[] args) {
		
		System.out.println();//화면의 출력
		System.out.println("하이!! ");//문자열 데이터
		System.out.println(3.14);//실수 데이터
		System.out.println(100);//정수 데이터
		System.out.println(34+567);//수식 데이터
		System.out.println(true);//논리(boolean) 데이터
		System.out.println("하이!!"+"자바야~");//문자열+문자열은 연결
		System.out.println("나는"+23+"세입니다");//문자열+정수는 연

	}

}


